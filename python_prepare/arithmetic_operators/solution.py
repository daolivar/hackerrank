# This block ensures that the following code runs only when this script is executed directly
if __name__ == '__main__':
    # Read an integer input from the user and assign it to variable 'a'
    a = int(input())
    
    # Read a second integer input from the user and assign it to variable 'b'
    b = int(input())
    
    # Print the sum of 'a' and 'b'
    print(a + b)
    
    # Print the difference of 'a' and 'b' (a minus b)
    print(a - b)
    
    # Print the product of 'a' and 'b'
    print(a * b)
